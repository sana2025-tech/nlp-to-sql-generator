def fix_sql(user_question: str, broken_sql: str, error_message: str) -> str:
    """
    Sends the failed SQL and its error back to the LLM, asking it to fix it.
    """
    fix_prompt = f"""The following SQL query failed when executed against the database.

Original question: {user_question}

SQL that failed:
{broken_sql}

Error message from the database:
{error_message}

Fix the SQL query so it runs correctly. Follow the same rules as before:
- Output ONLY the corrected SQL query, nothing else.
- Use only tables and columns from the schema provided earlier.
- Only SELECT statements are allowed.
"""

    response = client.chat.completions.create(
        model="llama-3.3-70b-versatile",
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": fix_prompt}
        ],
        temperature=0
    )

    fixed_sql = response.choices[0].message.content.strip()
    fixed_sql = fixed_sql.replace("```sql", "").replace("```", "").strip()

    return fixed_sql